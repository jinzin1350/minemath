export async function seedDictationWords() {
  console.log("🌱 Seeding dictation words...");
  
  try {
    // Insert words in batches to avoid overwhelming the database
    const batchSize = 100;
    for (let i = 0; i < wordsList.length; i += batchSize) {
      const batch = wordsList.slice(i, i + batchSize);
      await db.insert(dictationWords).values(batch).onConflictDoNothing();
      console.log(`✅ Inserted batch ${Math.floor(i / batchSize) + 1} (${batch.length} words)`);
    }
    
    console.log(`🎉 Successfully seeded ${wordsList.length} words!`);
  } catch (error) {
    console.error("❌ Error seeding dictation words:", error);
    throw error;
  }
}

