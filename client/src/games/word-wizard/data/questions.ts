// Helper function to get questions by level
export const getQuestionsByLevel = (level: number): Question[] => {
  return questions.filter((q) => q.level === level);
};

// Helper function to get random question from a level
export const getRandomQuestionForLevel = (level: number): Question | null => {
  const levelQuestions = getQuestionsByLevel(level);
  if (levelQuestions.length === 0) return null;
  return levelQuestions[Math.floor(Math.random() * levelQuestions.length)];
};
